     public void llenarTabla(){
        modelo.setRowCount(0);
        List<DTODetalleInforme> lista = controladorHistorial.buscarHistorialPorPaciente(idPaciente, tipoInforme);
        for(DTODetalleInforme dto : lista){
            modelo.addRow(new Object[]{dto.getId(),dto.getFechaElaboracion(),dto.getInformeMedico(),dto.getPersonal()});
            
        }
    }
